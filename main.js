function choosePlan() {
    let localCart = JSON.parse(localStorage.getItem("cart"));
    let planMenu = document.querySelector(".planMenu");
    let nameFriend = localStorage.getItem("nameFriend");
    planMenu.innerHTML = null;
    planMenu.style.background = '#fff';

    document.getElementById('titleChoosePlan').innerHTML = "Kies hier jouw keuzepakket voor de voucher van " + nameFriend + " en jou";

    let oPlan = JSON.parse(localStorage.getItem("personalOptionsPlan"));
    console.log(oPlan);
    oPlan.map(function (elem) {
        //select menu
        let div = document.createElement("div");
        let divDetail = document.createElement("divDetail");
        let divButton = document.createElement("divButton");

        // let imgDiv = document.createElement("div");
        // let img = new Image();
        // img.src = elem.img_url;
        // console.log(elem.img_url);
        // imgDiv.append(img);
        if (elem.titlePlan === advisePlan) {
            div.style.backgroundColor = '#00B050';
        }

        let titlePlan = document.createElement("h4");
        titlePlan.innerHTML = `${elem.titlePlan} Bungel Deal`;

        let shortDescription = document.createElement("p");
        shortDescription.innerHTML = elem.shortDescription;

        let price = document.createElement("h4");
        price.innerHTML = ` ${elem.price} te ontvolgen bungel(s) / per voucher`;

        let specifications = document.createElement("h5");
        specifications.innerHTML = "Voucher is inclusief:"

        let nrSpecifications = document.createElement("ul");
        for (i = 0; i < elem.specifications.length; ++i) {
            let li = document.createElement('li');
            li.innerText = elem.specifications[i];
            nrSpecifications.appendChild(li);
        }
        //nrSpecifications.innerHTML = `${elem.specifications}`;

        let button = document.createElement("button");
        button.innerHTML = "Selecteer plan";

        //detailDiv.append(titlePlan, price, nrSpecifications);
        divDetail.append(titlePlan, shortDescription, price, specifications, nrSpecifications);
        divButton.append(button);
        div.append(divDetail, divButton)
        document.querySelector(".planMenu").append(div);
        //document.querySelector(".specificationMenu").append(divButton);

        //let localCart = JSON.parse(localStorage.getItem("cart"));
        button.addEventListener("click", function () {
            if (localCart.length === 0){
                localCart = Array.apply(null, Array(elem.nrSpecifications)).map(function () {})
            } else if (localCart.length > elem.nrSpecifications){
                let sum = localCart.length - elem.nrSpecifications;
                for (i = 0; i < sum; i++){
                    localCart.pop();
                }
            } else if (localCart.length < elem.nrSpecifications){
                let sum = elem.nrSpecifications - localCart.length;
                for (i = 0; i < sum; i++){
                    //localCart.push({img_url: " ", name: " ", select: false, id: 0});
                    localCart.push(null);
                }
            }
            //let cartSpec = Array.apply(null, Array(elem.nrSpecifications)).map(function () {})
            localStorage.setItem("cart", JSON.stringify(localCart));
            localStorage.setItem("clickedNrSpecifications", elem.nrSpecifications);
            window.location.href ="./specifyPlan.html";
        });
    })
    //in myCredits advise plan
}

