            const divs = selectSpecMenu.querySelectorAll('div');

            //check if not already selected
            if (elem.select === false) {
                //set all items to false
                sInteraction.forEach(item => item.select = false);
                divs.forEach(div => {
                    div.style.backgroundColor = '#BFEBD3'
                })


                //check if all items are unselected and if so set one to true
                if (areAllItemsUnselected(sInteraction)) {
                    cartLocal[elem.id] = elem;
                    elem.select = !elem.select;
                    div.style.background = '#00B050';
                }
            } else {
                elem.select = !elem.select;
                div.style.backgroundColor = '#BFEBD3'
                cartLocal[elem.id] = null;
            }

            //remove item if sel again
            localStorage.setItem("cart", JSON.stringify(cartLocal));
            localStorage.setItem(selSpecifications[selSpec], JSON.stringify(sInteraction));
            console.log(JSON.parse(localStorage.getItem("cart")));

